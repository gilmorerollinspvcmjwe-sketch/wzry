export function getAchievementsByCategory(category: AchievementCategory): Achievement[] {
  return ACHIEVEMENTS.filter(a => a.category === category);
}

export function getAchievementsByRarity(rarity: AchievementRarity): Achievement[] {
  return ACHIEVEMENTS.filter(a => a.rarity === rarity);
}

export function getAchievementById(id: string): Achievement | undefined {
  return ACHIEVEMENTS.find(a => a.id === id);
}

export function getCategoryName(category: AchievementCategory): string {
  const names: Record<AchievementCategory, string> = {
    career: '职业生涯',
    competition: '比赛竞技',
    management: '俱乐部管理',
    development: '选手养成',
    social: '社交粉丝',
    special: '特殊成就',
  };
  return names[category];
}

export function getRarityName(rarity: AchievementRarity): string {
  const names: Record<AchievementRarity, string> = {
    common: '普通',
    uncommon: '罕见',
    rare: '稀有',
    epic: '史诗',
    legendary: '传奇',
  };
  return names[rarity];
}

export function getRarityColor(rarity: AchievementRarity): string {
  const colors: Record<AchievementRarity, string> = {
    common: '#999',
    uncommon: '#4caf50',
    rare: '#2196f3',
    epic: '#9c27b0',
    legendary: '#ffc107',
  };
  return colors[rarity];
}
